<a name="install"></a><h2>Установка расширения</h2>

<p>Расширение можно установить для следующих браузеров:</p>

<ul>
  <li>
    <a href="#install_google">Google Chrome</a>
  </li>
</ul>

<p>Чтобы установить расширение для браузера, следуйте инструкциям описанным ниже.</p>

<a name="install_google"></a><h3>Google Chrome</h3>

<ul>
  <li>
    Перейдите на страницу расширения в магазине Google Chrome по ссылке:
    <p>
      <a href="<?= URL_EXTENSION ?>" target="_blank"><?= URL_EXTENSION ?></a>
    </p>
  </li>
  <li>
    На открывшейся странице вы увидите описание расширения «Raznosilka»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-83.png') ?>" alt="Установка или включение расширения">
    </div>
  </li>
  <li>
    Нажмите на кнопку «Установить»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-84.png') ?>" alt="Установка или включение расширения">
    </div>
  </li>
  <li>
    После того как браузер запросит подтверждение установки, нажмите на кнопку «Установить расширение»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-85.png') ?>" alt="Установка или включение расширения">
    </div>
  </li>
  <li>
    Расширение установится, и иконка расширения появится в правом верхнем углу вашего браузера:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-86.png') ?>" alt="Установка или включение расширения">
    </div>
  </li>
</ul>

<a name="enable"></a><h2>Включение расширения</h2>

<p>Если вы уже установили расширение, но по какой-то причине его отключили и не знаете как включить, то следуйте инструкциям для своего браузера:</p>

<ul>
  <li>
    <a href="#enable_google">Google Chrome</a>
  </li>
</ul>

<a name="enable_google"></a><h3>Google Chrome</h3>

<ul>
  <li>
    Перейдите на страницу управления расширениями по следующей ссылке:
    <p><a href="chrome://extensions/" target="_blank">chrome://extensions/</a></p>
    <blockquote>Если после нажатии по ссылке ничего не происходит, то скопируйте её и вставьте в адресную строку браузера.</blockquote>
  </li>
  <li>
    Найдите в списке расширение «Raznosilka» и нажмите на галочку «Включить»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-87.png') ?>" alt="Установка или включение расширения">
    </div>
  </li>
</ul>

