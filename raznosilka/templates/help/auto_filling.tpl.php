<h2>Автопроставление оплат</h2>

<p>На данном этапе происходит проставление оплат на сайт СП в автоматическом режиме:</p>

<div class="img-wrapper">
  <img src="<?= URL::to('/files/images/help/image-22.png') ?>" alt="Автопроставление оплат">
</div>

<blockquote>Не закрывайте страницу пока оплаты не будут полностью проставлены.</blockquote>

<p>После окончания процесса автопроставления оплат будет выведена надпись о том, что атопроставление оплат завершено:</p>

<div class="img-wrapper">
  <img src="<?= URL::to('/files/images/help/image-23.png') ?>" alt="Автопроставление оплат">
</div>

<p>По окончанию автопроставления оплат «Разносилка» сверяет общую сумму, которая <i>должна быть проставлена</i> на сайт СП, с той суммой, которая <i>фактически проставлена на сайте СП</i>. Если эти суммы совпадают, то сумма найденная «Разносилкой» окрашивается в <span class="success bold">зелёный цвет</span>.</p>

<p>Далее вы можете перейти в «<a href="<?= URL::to('help/purchase') ?>">Обзор закупки</a>» для получения более полной информации о состоянии закупки:</p>

<div class="img-wrapper">
  <img src="<?= URL::to('/files/images/help/image-29.png') ?>" alt="Автопроставление оплат">
</div>

