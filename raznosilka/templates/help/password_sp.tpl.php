<h1>Смена логина и пароля от сайта СП</h1>

<blockquote>Если для соединения с сайтом СП используется расширение для браузера, то пропустите данный раздел, так как менять логин и пароль от сайта СП не требуется.</blockquote>

<p>Если у вас сменился логин или пароль на сайте СП, то в «Разносилке» так же нужно обновить учётные данные для доступа к сайту СП.</p>
<p>Для того чтобы сменить логин и пароль от сайта СП, нужно:</p>
<ul>
  <li>
    Зайти в «<a href="<?= URL::to('user') ?>" target="_blank">Настройки аккаунта</a>» через боковое меню:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-60.png') ?>" alt="Смена логина и пароля от сайта СП">
    </div>
  </li>
  <li>
    Далее нажать на ссылку «<a href="<?= URL::to('user/sp') ?>" target="_blank">Настройки сайта СП</a>»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-61.png') ?>" alt="Смена логина и пароля от сайта СП">
    </div>
  </li>
  <li>
    На открывшейся странице нажать на ссылку «<a href="<?= URL::to('user/password') ?>" target="_blank">Смена логина и пароля к сайту СП</a>»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-62.png') ?>" alt="Смена логина и пароля от сайта СП">
    </div>
  </li>
  <li>
    Теперь введите новый логин и пароль для доступа к сайту СП и нажмите кнопку «Сохранить»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-63.png') ?>" alt="Смена логина и пароля от сайта СП">
    </div>
  </li>
  <li>
    Если смена логина и пароля от сайта СП прошла успешно, то вы увидите соответствующее сообщение:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-64.png') ?>" alt="Смена логина и пароля от сайта СП">
    </div>
  </li>
</ul>

