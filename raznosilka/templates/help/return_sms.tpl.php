<h1>Возврат SMS</h1>

<p>С помощью инструмента «Возврат SMS» вы можете отметить те SMS, которые больше не должны использоваться для проставления оплат. Эта необходимость может возникнуть, если платёж связанный с SMS был возвращён участнику закупки.</p>
<p>Перейти в инструмент «Возврат SMS» можно несколькими способами:</p>

<ul>
  <li>
    На главной странице «Разносилки» нажать на кнопку «Возврат SMS»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-54.png') ?>" alt="Возврат SMS">
    </div>
  </li>
  <li>
    В боковом меню нажать на кнопку «Возврат SMS»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-55.png') ?>" alt="Возврат SMS">
    </div>
  </li>
</ul>

<p>Для того чтобы найти SMS, которую необходимо отметить как возвращённую, нужно выполнить следующие шаги:</p>

<ul>
  <li>
    Указать данные для поиска SMS. После чего нажать на кнопку «Найти SMS»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-56.png') ?>" alt="Возврат SMS">
    </div>
  </li>
  <li>
    Если в результатах поиска нет нужной вам SMS или найдено слишком много SMS, то уточните критерии поиска.
  </li>
  <li>
    Если среди результатов поиска вы нашли нужную вам SMS, то нажмите на кнопку «Отметить найденную SMS как возвращённую» напротив нужной вам SMS:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-57.png') ?>" alt="Возврат SMS">
    </div>
  </li>
  <li>
    После чего SMS сменит свой статус и будет не доступна для проставления оплат:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-58.png') ?>" alt="Возврат SMS">
    </div>
  </li>
  <li>
    Для того чтобы отменить возврат SMS, нужно нажать на кнопку «Удалить отметку о том, что SMS возвращена»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-59.png') ?>" alt="Возврат SMS">
    </div>
  </li>
</ul>

