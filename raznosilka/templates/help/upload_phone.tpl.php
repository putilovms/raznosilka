<h1>Загрузка SMS с телефона</h1>

<p>Загрузка SMS с телефона в «Разносилку» осуществляется точно так же как и с <a href="<?= URL::to('help/upload_pc') ?>">компьютера</a>. Единственное отличие заключается в том, что файл необходимо выбрать через диспетчер файлов, который установлен у вас на телефоне.</p>

<p>Делается это следующим образом:</p>

<ul>
  <li>
    Нажать на кнопку «Выбрать файлы»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-25.png') ?>" alt="Загрузка SMS">
    </div>
  </li>
  <li>
    В появившемся окне выбрать «Диспетчер файлов»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-26.png') ?>" alt="Загрузка SMS">
    </div>
  </li>
  <li>
    В диспетчере файлов перейти в папку с сохранёнными SMS и выбрать нужный файл.
  </li>
  <li>
    Нажать кнопку «Загрузить»:
    <div class="img-wrapper">
      <img src="<?= URL::to('/files/images/help/image-27.png') ?>" alt="Загрузка SMS">
    </div>
  </li>
</ul>

